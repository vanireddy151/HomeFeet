const normalizePhone = (value = '') =>
  String(value || '').replace(/\D/g, '').slice(-10);

const splitEmailAndPhone = (email = '', phone = '') => {
  const combined = `${email} ${phone}`.trim();
  const emailMatch = combined.match(/[^\s]+@[^\s+]+/);
  const phoneMatch = combined.match(/\+?\s*91[\s-]*\d[\d\s-]{8,}/);
  return {
    email: emailMatch ? emailMatch[0].replace(/\+91.*$/, '') : email,
    phone: phoneMatch ? phoneMatch[0] : phone
  };
};

